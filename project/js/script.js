// Función para manejar la navegación
function handleNavigation() {
  const currentPage = window.location.pathname.split("/").pop()

  switch (currentPage) {
    case "post-detail.html":
      renderPostDetail()
      break
    case "review-detail.html":
      renderReviewDetail()
      break
    case "playlist-detail.html":
      renderPlaylistDetail()
      break
    default:
      // Si estamos en la página principal, renderizamos el contenido normal
      renderFeaturedPosts()
      renderLatestReviews()
      renderPlaylists()
  }
}

document.addEventListener("DOMContentLoaded", () => {
  handleNavigation()

  const hamburger = document.querySelector(".hamburger")
  const navMenu = document.querySelector(".nav-menu")

  hamburger.addEventListener("click", mobileMenu)

  function mobileMenu() {
    hamburger.classList.toggle("active")
    navMenu.classList.toggle("active")
  }

  const navLink = document.querySelectorAll(".nav-menu a")

  navLink.forEach((n) => n.addEventListener("click", closeMenu))

  function closeMenu() {
    hamburger.classList.remove("active")
    navMenu.classList.remove("active")
  }
})

function updateLinks() {
  const featuredPosts = document.querySelectorAll("#featured-posts .post-item")
  const latestReviews = document.querySelectorAll("#latest-reviews .review-item")
  const playlists = document.querySelectorAll("#playlists-content .playlist-item")

  featuredPosts.forEach((post, index) => {
    post.addEventListener("click", () => {
      window.location.href = `post-detail.html?id=${featuredPosts[index].querySelector("a").href.split("?id=")[1]}`
    })
  })

  latestReviews.forEach((review, index) => {
    review.addEventListener("click", () => {
      window.location.href = `review-detail.html?id=${index + 1}`
    })
  })

  playlists.forEach((playlist, index) => {
    playlist.addEventListener("click", () => {
      window.location.href = `playlist-detail.html?id=${index + 1}`
    })
  })
}

document.addEventListener("DOMContentLoaded", () => {
  if (window.location.pathname.endsWith("index.html") || window.location.pathname === "/") {
    renderFeaturedPosts()
    renderLatestReviews()
    renderPlaylists()
    updateLinks()
  }
})


